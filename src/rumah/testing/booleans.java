package rumah.testing;

public class booleans {
    public static void main(String[] args) {
        boolean A = true;
        boolean B = false;
        boolean perbandingan = 10 > 20;

        System.out.println("A = "+A);
        System.out.println("B = "+B);
        System.out.println("10>20 = "+perbandingan);
        System.out.println("20<40 = "+(20<40));
    }
}
